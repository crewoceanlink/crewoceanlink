import { supabase } from "../../../lib/supabase";

const PROTECT_MIN_GB = 0.5;
const PROTECT_PERCENT = 0.05;

export default async function handler(req, res) {
  const token = req.query.token;

  if (!process.env.ROUTER_SYNC_TOKEN || token !== process.env.ROUTER_SYNC_TOKEN) {
    return res.status(401).send("# unauthorized");
  }

  const { data, error } = await supabase
    .from("crew_vouchers")
    .select("voucher_code,status,gb_used,gb_total");

  if (error) {
    return res.status(500).send(`# Supabase error: ${error.message}`);
  }

  const lines = [
    "# CrewOceanLink voucher sync",
    "# generated by Vercel",
    "# protection mode: remaining <= max(5%, 0.5GB)",
    "# slow mode uses hotspot profile rate-limit",
  ];

  for (const voucher of data || []) {
    const code = String(voucher.voucher_code || "").trim().toUpperCase();
    const status = String(voucher.status || "").trim().toLowerCase();

    if (!code) continue;

    const gbUsed = Number(voucher.gb_used || 0);
    const gbTotal = Number(voucher.gb_total || 0);
    const remainingGb = Math.max(gbTotal - gbUsed, 0);
    const protectThresholdGb = Math.max(gbTotal * PROTECT_PERCENT, PROTECT_MIN_GB);

    const shouldProtect =
      gbTotal > 0 &&
      remainingGb > 0 &&
      remainingGb <= protectThresholdGb;

    const profile = shouldProtect ? "slow" : "default";

    lines.push(`:do { /queue tree remove [find name~"COL-${code}"] } on-error={}`);
    lines.push(`:do { /queue simple remove [find name~"COL-${code}"] } on-error={}`);
    lines.push(`:do { /ip firewall mangle remove [find comment="CrewOceanLink limit ${code}"] } on-error={}`);

    if (status === "active" || status === "unused") {
      lines.push(
        `:if ([:len [/ip hotspot user find name="${code}"]] = 0) do={ /ip hotspot user add name="${code}" password="${code}" profile=${profile} comment="CrewOceanLink auto" }`
      );

      lines.push(
        `:do { /ip hotspot user set [find name="${code}"] profile=${profile} } on-error={};
:local u [/ip hotspot user get [find name="${code}"] profile];
:if (($u != "slow") && ("${profile}" = "slow")) do={
  :local a [/ip hotspot active find user="${code}"];
  :if ([:len $a] > 0) do={ /ip hotspot active remove $a }
}`
      );

      lines.push(
        `:do { /ip hotspot user enable [find name="${code}"] } on-error={}`
      );
    }

    if (status === "exhausted" || status === "blocked" || status === "expired") {
      lines.push(
        `:local activeIds [/ip hotspot active find user="${code}"]; :if ([:len $activeIds] > 0) do={ /ip hotspot active remove $activeIds }`
      );

      lines.push(
        `:local cookieIds [/ip hotspot cookie find user="${code}"]; :if ([:len $cookieIds] > 0) do={ /ip hotspot cookie remove $cookieIds }`
      );

      lines.push(
        `:do { /ip hotspot user disable [find name="${code}"] } on-error={}`
      );
    }
  }

  res.setHeader("Content-Type", "text/plain");
  return res.status(200).send(lines.join("\n"));
}